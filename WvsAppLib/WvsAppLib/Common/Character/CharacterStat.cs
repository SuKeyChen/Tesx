    public struct CharacterStat
    {
        #region Local Variables
        public readonly int CharacterID;
        public readonly string CharacterName; //max char. length: 13
        public readonly sbyte Gender, Portal;
        public readonly int Skin, Hair, Face, MixBaseHairColor, MixAddHairColor, MixHairBaseProb;
        public readonly int Level, Job, Str, Dex, Int, Luk, Hp, MHp, Mp, MMp, Ap, Sp;
        public readonly long Exp, Money, Wp;
        public readonly bool IsBurningCharacter;
        #endregion

        #region Constructor
        public CharacterStat(int characterID, string characterName, int level,
                            sbyte gender, int skin, int hair,
                            int face, int mixBaseHairColor, int mixAddHairColor,
                            int mixHairBaseProb, int job, int str,
                            int dex, int int_, int luk,
                            int hp, int mHp, int mp,
                            int mMp, int ap, int sp,
                            int wp, long exp, long money,
                            sbyte portal,
                            bool isBurningChar)
        {
            this.CharacterID = characterID;
            this.CharacterName = characterName;
            this.Level = level;
            this.Gender = gender;
            this.Skin = skin;
            this.Hair = hair;
            this.Face = face;
            this.MixBaseHairColor = mixBaseHairColor;
            this.MixAddHairColor = mixAddHairColor;
            this.MixHairBaseProb = mixHairBaseProb;
            this.Job = job;
            this.Str = str;
            this.Dex = dex;
            this.Int = int_;
            this.Luk = luk;
            this.Hp = hp;
            this.MHp = mHp;
            this.Mp = mp;
            this.MMp = mMp;
            this.Ap = ap;
            this.Sp = sp;
            this.Wp = wp;
            this.Exp = exp;
            this.Money = money;
            this.Portal = portal;
            this.IsBurningCharacter = isBurningChar;
        }
        #endregion

        #region Methods
        public void Encode() { }
        public void EncodeChangeStat() { }
        public void Decode() { }
        public void EncodeMoney() { }
        #endregion
    }

